/* === FUNCOES DE APOIO | inicio === */
export function buscarConfiguracaoCredenciamento(
  configuracoes: ConfiguracaoCredenciamento[],
  id?: string,
): ConfiguracaoCredenciamento {
  return (
    configuracoes.find((configuracao) => configuracao.id === id) ??
    configuracoes.find((configuracao) => configuracao.ativo) ??
    configuracoes[0] ??
    CONFIGURACOES_CREDENCIAMENTO_PADRAO[0]
  );
}

export function filtrarConfiguracoesPublicas(
  configuracoes: ConfiguracaoCredenciamento[],
  publico: PublicoAlvoConfiguracao,
): ConfiguracaoCredenciamento[] {
  const filtradas = configuracoes.filter(
    (configuracao) =>
      configuracao.ativo &&
      (configuracao.publicoAlvo === publico || configuracao.publicoAlvo === 'ambos'),
  );

  return filtradas.length > 0 ? filtradas : configuracoes.filter((configuracao) => configuracao.ativo);
}

export function normalizarConfiguracoesCredenciamento(
  configuracoes: ConfiguracaoCredenciamento[],
): ConfiguracaoCredenciamento[] {
  if (!Array.isArray(configuracoes) || configuracoes.length === 0) {
    return CONFIGURACOES_CREDENCIAMENTO_PADRAO;
  }

  return configuracoes.map((configuracao, indice) => ({
    ...CONFIGURACOES_CREDENCIAMENTO_PADRAO[0],
    ...configuracao,
    id: configuracao.id || `configuracao-${indice + 1}`,
    ativo: typeof configuracao.ativo === 'boolean' ? configuracao.ativo : true,
    nome: configuracao.nome || `Configuração ${indice + 1}`,
    publicoAlvo: configuracao.publicoAlvo || 'ambos',
    cenarioRoi: configuracao.cenarioRoi || 'hospital_credenciando_medico',
    premissaSite: configuracao.premissaSite || configuracao.descricao || 'Premissa configurada no painel administrativo.',
  }));
}
